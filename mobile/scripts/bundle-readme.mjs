#!/usr/bin/env node
/**
 * Read the repo's root README.md and emit it as a TypeScript string module
 * at src/generated/readme.ts. Wired into the mobile workspace's `prestart`
 * and `prebuild` npm scripts so the bundled content always tracks the source.
 *
 * Generated output is .gitignored.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const REPO_ROOT = path.resolve(__dirname, '..', '..')
const SOURCE = path.join(REPO_ROOT, 'README.md')
const OUT_DIR = path.resolve(__dirname, '..', 'src', 'generated')
const OUT_FILE = path.join(OUT_DIR, 'readme.ts')

if (!fs.existsSync(SOURCE)) {
  console.error(`bundle-readme: source not found at ${SOURCE}`)
  process.exit(1)
}
const raw = fs.readFileSync(SOURCE, 'utf8')
fs.mkdirSync(OUT_DIR, { recursive: true })

// Escape backticks and ${ for safe template-literal embedding.
const escaped = raw.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${')

const banner =
  `// AUTO-GENERATED by mobile/scripts/bundle-readme.mjs — do not edit by hand.\n` +
  `// Run \`pnpm start\` or \`pnpm prebuild\` to refresh.\n`
const body = `export const readme = \`${escaped}\`\nexport default readme\n`

fs.writeFileSync(OUT_FILE, banner + body, 'utf8')

const lines = raw.split('\n').length
console.log(`bundle-readme: wrote ${OUT_FILE} (${raw.length} bytes, ${lines} lines)`)
